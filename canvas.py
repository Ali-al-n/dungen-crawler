import actor
import log


def canvas():
    log.clear()
    try:
        temp = actor.player1.location
        temp_HP = str(actor.player1.hitpoints)
        temp_Level = str(actor.player1.level)
        temp_Exp = str(actor.player1.experience)
        temp_Stamina = str(actor.player1.stamina)
        temp_Gold = str(actor.player1.gold)
        temp_AP = str(actor.player1.AP)
        temp_DP = str(actor.player1.DP)
        inv_size = actor.player1.getInventorySize()
        inventory = actor.player1.showInventory()
    except:
        temp = EMPTY_PIC
        temp_HP = str(0)
        temp_Level = str(0)
        temp_Exp = str(0)
        temp_Stamina = str(0)
        temp_AP = str(0)
        temp_DP = str(0)
        temp_Gold = str(0)
        inv_size = str(0)
        inventory = [""]
    if not temp:
        temp = EMPTY_PIC
    status = [
        "######################",
        " HP              : " + temp_HP,
        " Level           : " + temp_Level,
        " AP              : " + temp_AP,
        " DP              : " + temp_DP,
        " Gold            : " + temp_Gold,
        " Experience      : " + temp_Exp,
        " Stamina         : " + temp_Stamina,
        "######################",
    ]

    remainder = inv_size
    for i in range(len(temp)):
        # append status to canvas
        if i <= (len(status) - 1):
            print(temp[i] + status[i])

        # append inventory to canvas
        if i > len(status) - 1 and remainder != 0:
            print(
                temp[i]
                + " %i. " % (i - len(status) + 1)
                + inventory[i - len(status)].name
            )
            remainder -= 1

        # draw remainder of canvas
        elif i > len(status) - 1 and remainder == 0:
            print(temp[i])

    log.displayHistory()


